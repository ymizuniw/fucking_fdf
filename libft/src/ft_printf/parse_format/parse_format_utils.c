#include "ft_printf.h"

int	is_flag(const char c)
{
	return (c == '-' || c == '0' || c == '+' || c == ' ' || c == '#');
}

int	is_specifier(const char c)
{
	return (c == 'c' || c == 's' || c == 'd' || c == 'i' || c == 'u' || c == 'x'
		|| c == 'X' || c == 'p' || c == '%');
}
